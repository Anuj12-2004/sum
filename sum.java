public class SumOfNumbers {
    public static void main(String[] args) {
        // Declare two variables
        int num1 = 5;
        int num2 = 10;
        
        // Calculate the sum
        int sum = num1 + num2;
        
        // Print the result to the console
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
